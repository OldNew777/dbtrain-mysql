CREATE DATABASE THU;
USE THU;
CREATE TABLE ORDERS (
    O_ORDERKEY        INT,
    O_CUSTKEY         INT NOT NULL,
    O_ORDERSTATUS     VARCHAR(1),
    O_TOTALPRICE      FLOAT,
    O_ORDERDATE       DATE,
    O_ORDERPRIORITY   VARCHAR(15),
    O_CLERK           VARCHAR(15),
    O_SHIPPRIORITY    INT,
    O_COMMENT         VARCHAR(79)
);
DESC ORDERS;
